# Math Operators

# Addition
x = 5
y = 3
print(x + y) # prints 8

# Subtraction
print(x - y) # prints 2

# Multiplication
print(x * y) # prints 15

# Floating Point Division
x = 15
y = 3
print(x / y) # prints 5.0

# Integer Divison
x = 5
y = 2
print(x // y) # prints 2

# Modulus
x = 14
y = 3
print(x % y) # prints 2

# Exponent
x = 2
y = 3
print(x ** y) # prints 8