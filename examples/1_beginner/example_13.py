# Range

# stop
print("range(stop)")
for i in range(5):
    print(i)

print()

# start, stop
print("range(start, stop)")
for i in range(1, 5):
    print(i)

print()

# start, stop, step
print("range(start, stop, step)")
for i in range(2, 5, 2):
    print(i)

print()

# negative step
print("negative step")
for i in range(5, 0, -1):
    print(i)

print()

# using for loops to repeat code
# a certain number of times
for i in range(5):
    print("May the Force be with you")