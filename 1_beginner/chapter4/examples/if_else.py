# If Statements

x = 5

# if
if x == 5:
    print("x is equal to 5")

# if, else
if x == 3:
    print("x is equal to 3")
else:
    print("x is NOT equal to 3")

# if, elif, else
if x == 5:
    print("x is equal to 5")
elif x == 6:
    print("x is equal to 6")
elif x == 7:
    print("x is equal to 7")
else:
    print("x is not equal to 5, 6, or 7")
